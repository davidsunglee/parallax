/**
 * API Conformance Suite — **transactions + audit writes** family (Phase 10c): the
 * write surface (`px.transaction(async tx => tx.<entity>.create / update /
 * terminate)`, spec §4.1) written as a developer would, run against `postgres:17`
 * through the SHIPPED `@parallax/db-postgres` adapter, asserting the corpus's
 * `expectedTableState` (write sequences) or `expectRows` (read-your-own-writes).
 *
 * Writes are NOT read-shaped, so the `assertSameOperation` no-drift guard does not
 * apply; the guarantee is that the developer's write code leaves the DB in the
 * corpus's final state. The DML is generated by the pure M7 / M8 write generators
 * the runtime already owns (no reinvented DML, no grader code):
 *  - audit-only (Balance) create / update / terminate chain milestones (`0510`–`0512`),
 *    each transaction stamped with a fixed clock so the processing instants are the
 *    corpus's authored instants;
 *  - non-temporal batched writes flush set-based + FK-safe at commit (`0604` /
 *    `0612` / `0613`);
 *  - a dependent in-transaction find observes a buffered insert (`0607`);
 *  - a non-temporal timestamp insert stores UTC / µs precision (`0004` / `0005`).
 */

import { execFileSync } from "node:child_process";
import { ParallaxDecimal, Temporal } from "@parallax/core";
import { postgresDialect } from "@parallax/dialect";
import { afterAll, beforeAll, expect, describe as group, it } from "vitest";
import { PostgresProvider } from "../../src/conformance/postgres-provider.js";
import { AttributeExpression } from "../../src/index.js";
import { assertRows, assertTableState, provisionCase } from "./_harness.js";
import { TRANSACTIONS } from "./covered.js";

const attr = (ref: string): AttributeExpression => new AttributeExpression(ref);
const dec = (text: string): ParallaxDecimal => ParallaxDecimal.from(text);
const at = (iso: string): Temporal.Instant => Temporal.Instant.from(iso);

const Balance = { id: attr("Balance.id"), value: attr("Balance.value") };
const Account = { id: attr("Account.id"), balance: attr("Account.balance") };
const Wallet = { id: attr("Wallet.id"), balance: attr("Wallet.balance") };

/** True when a Docker daemon is reachable (gates the Testcontainers lane). */
function dockerAvailable(): boolean {
  try {
    execFileSync("docker", ["info"], { stdio: "ignore", timeout: 10_000 });
    return true;
  } catch {
    return false;
  }
}

const HAS_DOCKER = dockerAvailable();

it("the transactions suite covers exactly the TRANSACTIONS family", () => {
  const covered = [
    "0004-timestamp-utc-normalization",
    "0005-timestamp-microsecond-precision",
    "0510-write-insert",
    "0511-write-update-chaining",
    "0512-write-terminate",
    "0604-batched-write",
    "0607-read-your-own-writes",
    "0608-rollback-discards-writes",
    "0612-fk-insert-ordering",
    "0613-batched-update-per-key",
  ];
  expect(covered.sort()).toEqual([...TRANSACTIONS].sort());
});

group.skipIf(!HAS_DOCKER)("transactions suite (Testcontainers postgres:17)", () => {
  const BOOT_TIMEOUT = 600_000;
  let provider: PostgresProvider;

  beforeAll(async () => {
    provider = await PostgresProvider.start();
  }, BOOT_TIMEOUT);

  afterAll(async () => {
    await provider?.close();
  });

  it(
    "0004: a timestamp insert stores as UTC",
    async () => {
      const f = await provisionCase(provider, "0004-timestamp-utc-normalization");
      await f.px.transaction(async (tx) => {
        await tx.entity("Event").create({ id: 1n, occurredAt: at("2024-03-01T05:30:00+05:30") });
      });
      await assertTableState(f.db, f.loaded, f.metamodel);
    },
    BOOT_TIMEOUT,
  );

  it(
    "0005: a timestamp insert keeps microsecond precision",
    async () => {
      const f = await provisionCase(provider, "0005-timestamp-microsecond-precision");
      await f.px.transaction(async (tx) => {
        await tx
          .entity("Event")
          .create({ id: 1n, occurredAt: at("2024-03-01T12:00:00.123456+00:00") });
      });
      await assertTableState(f.db, f.loaded, f.metamodel);
    },
    BOOT_TIMEOUT,
  );

  it(
    "0510: create opens an audit milestone [txInstant, infinity)",
    async () => {
      const f = await provisionCase(provider, "0510-write-insert", {
        now: "2024-01-01T00:00:00+00:00",
      });
      await f.px.transaction(async (tx) => {
        await tx.entity("Balance").create({ id: 1n, acctNum: "A", value: dec("100.00") });
      });
      await assertTableState(f.db, f.loaded, f.metamodel);
    },
    BOOT_TIMEOUT,
  );

  it(
    "0511: update closes the current milestone and chains a new one (audit trail)",
    async () => {
      const f = await provisionCase(provider, "0511-write-update-chaining", {
        now: "2024-01-01T00:00:00+00:00",
      });
      // Milestone 1: open at 2024-01-01 (the first transaction's processing instant).
      await f.px.transaction(async (tx) => {
        await tx.entity("Balance").create({ id: 1n, acctNum: "A", value: dec("100.00") });
      });
      // A LATER transaction (a fresh clock at 2024-06-01) updates the balance: the
      // runtime closes the current row (out_z = 2024-06-01) and chains a new current
      // row carrying the new value — the prior value survives as a superseded milestone.
      const later = await withClock(f, "2024-06-01T00:00:00+00:00");
      await later.transaction(async (tx) => {
        await tx
          .entity("Balance")
          .update(Balance.id.eq(1), { set: [Balance.value.set(dec("150.00"))] });
      });
      await assertTableState(f.db, f.loaded, f.metamodel);
    },
    BOOT_TIMEOUT,
  );

  it(
    "0512: terminate closes the current milestone and inserts nothing",
    async () => {
      const f = await provisionCase(provider, "0512-write-terminate", {
        now: "2024-01-01T00:00:00+00:00",
      });
      await f.px.transaction(async (tx) => {
        await tx.entity("Balance").create({ id: 1n, acctNum: "A", value: dec("100.00") });
      });
      const later = await withClock(f, "2024-08-01T00:00:00+00:00");
      await later.transaction(async (tx) => {
        await tx.entity("Balance").terminate(Balance.id.eq(1));
      });
      await assertTableState(f.db, f.loaded, f.metamodel);
    },
    BOOT_TIMEOUT,
  );

  it(
    "0604: buffered inserts + updates flush as set-based SQL",
    async () => {
      const f = await provisionCase(provider, "0604-batched-write");
      await f.px.transaction(async (tx) => {
        const wallets = tx.entity("Wallet");
        await wallets.create({ id: 10n, owner: "Mira", balance: dec("100.00") });
        await wallets.create({ id: 11n, owner: "Omar", balance: dec("20.00") });
        await wallets.create({ id: 12n, owner: "Nell", balance: dec("30.00") });
        await wallets.update(Wallet.id.eq(10), { set: [Wallet.balance.set(dec("500.00"))] });
        await wallets.update(Wallet.id.eq(11), { set: [Wallet.balance.set(dec("500.00"))] });
      });
      await assertTableState(f.db, f.loaded, f.metamodel);
    },
    BOOT_TIMEOUT,
  );

  it(
    "0607: a dependent find observes the buffered insert (read-your-own-writes)",
    async () => {
      const f = await provisionCase(provider, "0607-read-your-own-writes");
      const observed = await f.px.transaction(async (tx) => {
        const accounts = tx.entity("Account");
        await accounts.create({ id: 7n, owner: "Newton", balance: dec("5.00"), version: 1 });
        // The dependent find MUST observe the just-written row (a forced flush precedes it).
        return accounts.find(Account.id.eq(7)).toArray();
      });
      assertRows(observed, f.loaded, "Account", f.metamodel);
    },
    BOOT_TIMEOUT,
  );

  it(
    "0608: an aborted transaction discards its writes (rollback)",
    async () => {
      const f = await provisionCase(provider, "0608-rollback-discards-writes");
      const abort = new Error("abort the unit of work");
      // A write + a dependent find (forcing the RYOW flush), then a throw: the whole
      // unit of work maps onto ONE atomic scope, so the throw rolls the write back.
      await expect(
        f.px.transaction(async (tx) => {
          const accounts = tx.entity("Account");
          // Read account 1 first (records the observed version, M10), then update it.
          await accounts.find(Account.id.eq(1)).single();
          await accounts.update(Account.id.eq(1), { set: [Account.balance.set(dec("999.00"))] });
          const midTx = await accounts.find(Account.id.eq(1)).toArray();
          expect(midTx).toHaveLength(1);
          throw abort;
        }),
      ).rejects.toBe(abort);
      // After the abort, a fresh find re-resolves and MUST observe the ORIGINAL rows.
      const observed = await f.px.transaction(async (tx) =>
        tx.entity("Account").find(Account.id.eq(1)).toArray(),
      );
      assertRows(observed, f.loaded, "Account", f.metamodel);
    },
    BOOT_TIMEOUT,
  );

  it(
    "0612: a referenced parent is inserted before its child (FK ordering)",
    async () => {
      const f = await provisionCase(provider, "0612-fk-insert-ordering");
      await f.px.transaction(async (tx) => {
        await tx.entity("Order").create({
          id: 100n,
          name: "Hopper",
          sku: "X-1",
          qty: 1,
          price: dec("9.99"),
          active: true,
          orderedOn: Temporal.PlainDate.from("2024-07-01"),
        });
        await tx.entity("OrderItem").create({ id: 200n, orderId: 100n, sku: "X-1", quantity: 3 });
      });
      await assertTableState(f.db, f.loaded, f.metamodel);
    },
    BOOT_TIMEOUT,
  );

  it(
    "0613: distinct new values flush as one keyed UPDATE per key",
    async () => {
      const f = await provisionCase(provider, "0613-batched-update-per-key");
      await f.px.transaction(async (tx) => {
        const wallets = tx.entity("Wallet");
        await wallets.update(Wallet.id.eq(1), { set: [Wallet.balance.set(dec("111.00"))] });
        await wallets.update(Wallet.id.eq(2), { set: [Wallet.balance.set(dec("222.00"))] });
      });
      await assertTableState(f.db, f.loaded, f.metamodel);
    },
    BOOT_TIMEOUT,
  );
});

/** Rebuild a `px` on the same provisioned DB with a fixed clock (a later transaction). */
async function withClock(
  fixture: Awaited<ReturnType<typeof provisionCase>>,
  now: string,
): Promise<import("../../src/index.js").Parallax> {
  const { createParallax } = await import("../../src/index.js");
  return createParallax({
    database: fixture.db,
    descriptor: fixture.loaded.descriptor,
    dialect: postgresDialect,
    clock: { now: () => now },
  });
}
